// Literal and Variable
// Literal
console.log("This is class-04");

// variable
let a = 10;
console.log(a);

a = 20;
console.log(a);

a = "Javascript";
console.log(a);

// assignment operator
let maths = 96;
let science = 94;

// Arithmetic operator
console.log(maths + science);

// algebraic
const total = maths + science;
console.log(total + 10);

// String Operator
let firstName = "Nethra";
let lastName = "Gokul";

console.log(firstName + " " + lastName);
console.log(firstName.length);
console.log(firstName.toLocaleUpperCase());

// Conditional operator
let marks = 54;

console.log(marks > 40);

// Logical operator
let attendance = 80;
marks = 60;

console.log(attendance > 85 && marks > 40);
console.log(attendance > 85 || marks > 40);

// Type comparison
console.log(typeof 10);
console.log(typeof "10");
console.log("--------------------");
console.log(10 == "10");
console.log(10 === "10");
