// Data Types

// 1. Numbers
let age = 17;
let marks = 95.5;

console.log(age, marks);

// 2. String
let studentName = "Nethra";
console.log(studentName);

// 3. Boolean - (true, false)
let isPassed = true;
console.log(isPassed);

// 4. Array - storing multiple values in one variable
let subjects = ["math", "science", "english"];
console.log(subjects);
console.log(subjects[1]);

// 5. Object - store related data together (like a studentID card)
let student = {
    name: "Nethra",
    age: 17,
    grade: "12th",
};
console.log(student);
